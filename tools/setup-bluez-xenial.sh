### On Ubuntu 16.04 (xenial), it is necessary to update bluez version from the version provided by the default package list
if [ `cat /etc/lsb-release | grep xenial` ]; then
  sudo add-apt-repository ppa:janisozaur/bluez-xenial
  sudo apt-get update
  sudo apt-get install bluez
fi
